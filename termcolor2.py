

def red(string):
  return "\033[0;31m" + string + "\033[0m"

def green(string):
  return "\033[0;32m" + string + "\033[0m"

def yellow(string):
  return "\033[0;33m" + string + "\033[0m"

def grey(string):
  return "\033[0;90m" + string + "\033[0m"

def turquoise(string):
  return "\033[0;36m" + string + "\033[0m"
